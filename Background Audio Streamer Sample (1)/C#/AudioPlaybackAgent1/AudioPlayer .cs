        //Gets a bunch of strings
        public void AddToPlayListFromPodCastCollection(List<String> listOfUrls)
        {
            foreach (var url in listOfUrls)
            {
                if (!url.StartsWith("texttext"))
                {
                    string title, content;
                    SplitAndGetTitle(url, out title, out content);
                    AudioTrack t = new AudioTrack(new Uri(content), title, null, null, null, "880", enabledControls: EnabledPlayerControls.All);
                    _playList.Add(t);
                }
                else
                {
                    string content = url.Substring(8);
                    string title, contentsInIt;
                    SplitAndGetTitle(content, out title, out contentsInIt);
                    AudioTrack t = new AudioTrack(null, title, "text", contentsInIt, null, "880", enabledControls: EnabledPlayerControls.All);
                    _playList.Add(t);
                }
            }
        }
        public void SplitAndGetTitle(string fullString, out string title, out string content)
        {
            string[] stringSeparators = new string[] { "NoDontHaBooYah1234)(*&Noth!!!YSKS-f6b65218-ed8d-4daf-9836-ec9808db469f" };
            string[] titleAndContent = fullString.Split(stringSeparators, StringSplitOptions.None);
            if (titleAndContent.Length > 0)
            {
                content = titleAndContent[0];
            }
            else content = fullString;
            if (titleAndContent.Length > 1)
            {
                title = titleAndContent[1];
            }
            else title = "  ";



        }
